DOCTORS_DIR = os.path.join(os.path.dirname(os.path.abspath(__file__)), "doctors_photos")


def main():
    db.init_db()  # таблицы создаются, если их ещё нет

    missing = [photo for _, _, photo, _ in DOCTORS
               if not os.path.exists(os.path.join(DOCTORS_DIR, photo))]
    if missing:
        print("⚠️ Нет файлов фото в doctors_photos/:", ", ".join(missing))
        print("   Залейте папку doctors_photos/ на сервер и запустите снова.")
        sys.exit(1)

    db.clear_doctors()
    for full_name, title, photo, sort_order in DOCTORS:
        db.add_doctor(full_name, title, photo, sort_order)

    print(f"✅ Добавлено врачей: {len(DOCTORS)}")
    for d in db.get_all_doctors():
        print(f"  {d['sort_order']:>2}. {d['full_name']} — {d['title']}")


if __name__ == "__main__":
    main()
